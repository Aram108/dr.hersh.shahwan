/* ====================================
   FLAG TEMPLATES
   ==================================== */
const FLAG_TEMPLATES = {
    en: "🇬🇧",
    ar: "🇮🇶",
    ku: `<span class="flag-ku" aria-hidden="true">
         <span class="fk-red"></span>
         <span class="fk-white"><span class="fk-sun">☀</span></span>
         <span class="fk-green"></span>
       </span>`
};

const LANG_LABELS = { en: "EN", ar: "AR", ku: "KU" };

/* ====================================
   REGISTER GSAP PLUGINS FIRST
   ==================================== */
gsap.registerPlugin(ScrollTrigger);

/* ====================================
   DOM READY
   ==================================== */
document.addEventListener("DOMContentLoaded", () => {

    /* ---- Year ---- */
    document.getElementById("year").textContent = new Date().getFullYear();

    /* ============================================================
       THEME TOGGLE
       ============================================================ */
    const themeBtn = document.getElementById("theme-toggle");
    const themeIcon = document.getElementById("theme-icon");
    let theme = localStorage.getItem("theme") || "light";
    applyTheme(theme);

    themeBtn.addEventListener("click", () => {
        theme = theme === "light" ? "dark" : "light";
        applyTheme(theme);
        localStorage.setItem("theme", theme);
    });

    function applyTheme(t) {
        document.documentElement.dataset.theme = t;
        if (t === "dark") {
            themeIcon.classList.replace("ph-moon", "ph-sun");
        } else {
            themeIcon.classList.replace("ph-sun", "ph-moon");
        }
    }

    /* ============================================================
       NAVBAR SCROLL
       ============================================================ */
    const navbar = document.getElementById("navbar");
    const onScroll = () => navbar.classList.toggle("scrolled", window.scrollY > 60);
    window.addEventListener("scroll", onScroll, { passive: true });
    onScroll(); // run once on load

    /* ============================================================
       MOBILE MENU
       ============================================================ */
    const mobileOverlay = document.getElementById("mobile-menu");
    const openBtn = document.getElementById("mobile-toggle");
    const closeBtn = document.getElementById("close-mobile-menu");

    function openMobile() {
        mobileOverlay.classList.add("open");
        mobileOverlay.setAttribute("aria-hidden", "false");
        document.body.style.overflow = "hidden";
    }
    function closeMobile() {
        mobileOverlay.classList.remove("open");
        mobileOverlay.setAttribute("aria-hidden", "true");
        document.body.style.overflow = "";
    }

    openBtn.addEventListener("click", openMobile);
    closeBtn.addEventListener("click", closeMobile);
    document.querySelectorAll(".mobile-link").forEach(l => l.addEventListener("click", closeMobile));

    // Close on backdrop click
    mobileOverlay.addEventListener("click", e => {
        if (e.target === mobileOverlay) closeMobile();
    });

    /* ============================================================
       LANGUAGE DROPDOWN
       ============================================================ */
    const langDropdown = document.getElementById("lang-dropdown");
    const langBtn = document.getElementById("lang-btn");
    const langMenu = document.getElementById("lang-menu");
    const currentFlag = document.getElementById("current-flag");
    const currentLangEl = document.getElementById("current-lang");

    function openLangMenu() {
        langMenu.classList.add("open");
        langDropdown.classList.add("open");
        langBtn.setAttribute("aria-expanded", "true");
    }
    function closeLangMenu() {
        langMenu.classList.remove("open");
        langDropdown.classList.remove("open");
        langBtn.setAttribute("aria-expanded", "false");
    }

    langBtn.addEventListener("click", e => {
        e.stopPropagation();
        langMenu.classList.contains("open") ? closeLangMenu() : openLangMenu();
    });
    document.addEventListener("click", () => closeLangMenu());
    langMenu.addEventListener("click", e => e.stopPropagation());

    /* ============================================================
       I18N — SET LANGUAGE
       ============================================================ */
    let currentLang = localStorage.getItem("lang") || "en";
    let isPricesPanelOpen = false;   // track extra-prices state

    // Hoist all pricing toggle refs so updateMoreBtnText() works inside setLanguage()
    const toggleBtn = document.getElementById("toggle-more-prices");
    const extraPricesEl = document.getElementById("extra-prices");
    const moreBtnIcon = document.getElementById("more-btn-icon");
    const moreBtnText = document.getElementById("more-btn-text");

    function updateMoreBtnText() {
        if (!moreBtnText) return;
        moreBtnText.textContent = isPricesPanelOpen
            ? (T[currentLang].show_less || "Show Less")
            : (T[currentLang].and_more || "And more...");
    }

    function setLanguage(lang) {
        currentLang = lang;
        localStorage.setItem("lang", lang);

        // Direction
        const dir = (lang === "ar" || lang === "ku") ? "rtl" : "ltr";
        document.documentElement.setAttribute("dir", dir);
        document.documentElement.setAttribute("lang", lang);

        // Button label + flag
        currentFlag.innerHTML = FLAG_TEMPLATES[lang];
        currentLangEl.textContent = LANG_LABELS[lang];

        // Translate DOM
        document.querySelectorAll("[data-i18n]").forEach(el => {
            const key = el.dataset.i18n;
            if (T[lang]?.[key] !== undefined) {
                if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
                    el.placeholder = T[lang][key];
                } else {
                    el.textContent = T[lang][key];
                }
            }
        });

        // Fix more-prices button text to match current panel state
        updateMoreBtnText();

        // GSAP re-measure after text reflow
        setTimeout(() => ScrollTrigger.refresh(), 250);

        closeLangMenu();
    }

    document.querySelectorAll(".lang-menu li").forEach(li => {
        li.addEventListener("click", () => setLanguage(li.dataset.lang));
    });

    // Init
    setLanguage(currentLang);


    /* Pricing toggle now wired after hoisted declarations above */
    toggleBtn.addEventListener("click", () => {
        isPricesPanelOpen = !isPricesPanelOpen;
        extraPricesEl.classList.toggle("open", isPricesPanelOpen);
        toggleBtn.classList.toggle("open", isPricesPanelOpen);
        toggleBtn.setAttribute("aria-expanded", isPricesPanelOpen);
        updateMoreBtnText();
        setTimeout(() => ScrollTrigger.refresh(), 600);
    });

    /* ============================================================
       GSAP SCROLL ANIMATIONS
       ============================================================ */
    const fadeDefaults = { duration: 0.9, ease: "power3.out" };

    // Fade up
    gsap.utils.toArray(".gsap-up").forEach((el, i) => {
        gsap.fromTo(el,
            { y: 45, opacity: 0 },
            {
                y: 0, opacity: 1,
                ...fadeDefaults,
                delay: (i % 3) * 0.08,   // subtle stagger within row
                scrollTrigger: {
                    trigger: el,
                    start: "top 87%",
                    toggleActions: "play none none reverse"
                }
            }
        );
    });

    // Fade left → right
    gsap.utils.toArray(".gsap-left").forEach(el => {
        gsap.fromTo(el,
            { x: -55, opacity: 0 },
            {
                x: 0, opacity: 1, ...fadeDefaults,
                scrollTrigger: { trigger: el, start: "top 85%", toggleActions: "play none none reverse" }
            }
        );
    });

    // Fade right → left
    gsap.utils.toArray(".gsap-right").forEach(el => {
        gsap.fromTo(el,
            { x: 55, opacity: 0 },
            {
                x: 0, opacity: 1, ...fadeDefaults,
                scrollTrigger: { trigger: el, start: "top 85%", toggleActions: "play none none reverse" }
            }
        );
    });

    /* Hero entrance (on load, no scroll trigger) */
    gsap.fromTo(".hero-badge",
        { y: 20, opacity: 0 }, { y: 0, opacity: 1, duration: 0.8, ease: "power3.out", delay: 0.3 });
    gsap.fromTo(".hero-title",
        { y: 30, opacity: 0 }, { y: 0, opacity: 1, duration: 0.9, ease: "power3.out", delay: 0.5 });
    gsap.fromTo(".hero-subtitle",
        { y: 20, opacity: 0 }, { y: 0, opacity: 1, duration: 0.8, ease: "power3.out", delay: 0.7 });
    gsap.fromTo(".hero-actions",
        { y: 20, opacity: 0 }, { y: 0, opacity: 1, duration: 0.8, ease: "power3.out", delay: 0.9 });
    gsap.fromTo(".scroll-indicator",
        { opacity: 0 }, { opacity: 1, duration: 1, delay: 1.4 });

    /* Pricing cards stagger */
    gsap.utils.toArray(".price-card").forEach((card, i) => {
        gsap.fromTo(card,
            { y: 50, opacity: 0 },
            {
                y: 0, opacity: 1, duration: 0.8, ease: "power3.out",
                delay: i * 0.12,
                scrollTrigger: { trigger: ".pricing-grid", start: "top 82%", toggleActions: "play none none reverse" }
            }
        );
        // Remove the gsap-up class to prevent double animation
        card.classList.remove("gsap-up");
    });

    /* Work-card stagger (replaces old bento animation) */
    gsap.utils.toArray(".work-card").forEach((card, i) => {
        gsap.fromTo(card,
            { y: 30, opacity: 0 },
            {
                y: 0, opacity: 1, duration: 0.7, ease: "power3.out",
                delay: i * 0.1,
                scrollTrigger: { trigger: ".work-masonry", start: "top 82%", toggleActions: "play none none reverse" }
            }
        );
        card.classList.remove("gsap-up");
    });

    /* Work hero entrance */
    gsap.utils.toArray(".work-hero").forEach(el => {
        gsap.fromTo(el,
            { y: 40, opacity: 0 },
            {
                y: 0, opacity: 1, duration: 0.9, ease: "power3.out",
                scrollTrigger: { trigger: el, start: "top 85%", toggleActions: "play none none reverse" }
            }
        );
        el.classList.remove("gsap-up");
    });

});
